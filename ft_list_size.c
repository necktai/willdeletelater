#include "libft.h"

int		ft_list_size(t_list *begin_list)
{
	int		i;
	t_list	*tmp;

	i = 1;
	tmp = begin_list;
	while (tmp->next)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
