#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int check;

	check = 0;
	if (!s1 || !s2)
		return (0);
	if (*s1 == '\0' && *s2 == '\0')
		return (1);
	if (*s1 == '\0' || *s2 == '\0')
		return (0);
	check = ft_strcmp(s1, s2);
	if (check != 0)
		return (0);
	return (1);
}
