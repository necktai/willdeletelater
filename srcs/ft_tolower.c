#include "libft.h"

/*
** 97 - 122 inclusive, is a - z
** 65 - 90 inclusive, is A - Z
*/

int		ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
		return (c + 32);
	return (c);
}
