int		ft_is_prime(int nb)
{
	int tmp;

	if (nb <= 1)
	{
		return (0);
	}
	tmp = 2;
	while (tmp < (nb / tmp))
	{
		if ((nb % tmp) == 0)
		{
			return (0);
		}
		tmp++;
	}
	return (1);
}
